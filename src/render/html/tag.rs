use super::prelude::*;

pub fn write_tag_arg(buffer: &mut String, arg_name: &str, value: &str) -> Result<()> {
    write!(buffer, " {}", arg_name)?;
    buffer.push_str("=\"");
    escape_attr(buffer, value)?;
    buffer.push('"');

    Ok(())
}
